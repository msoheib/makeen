async function main() {
  const startTime = Date.now();
  
  console.log('ðŸŽ¯ QA Test Suite Configuration:');
  console.log(`   Timeout: ${testConfig.timeout}ms`);
  console.log(`   Retries: ${testConfig.retries}`);
  console.log(`   Workers: ${testConfig.workers}`);
  console.log(`   Screenshots: ${screenshotsDir}`);
  
  const results = [];
  
  // Run each test suite
  for (const suite of testSuites) {
    const result = await runTestSuite(suite);
    results.push({ ...suite, ...result });
  }
  
  // Generate comprehensive report
  await generateReport();
  
  const endTime = Date.now();
  const duration = Math.round((endTime - startTime) / 1000);
  
  console.log('\nðŸŽ‰ QA Test Suite Complete!');
  console.log(`   Total Duration: ${duration}s`);
  console.log(`   Successful Suites: ${results.filter(r => r.success).length}/${results.length}`);
  console.log(`   Failed Suites: ${results.filter(r => !r.success).length}/${results.length}`);
  
  if (results.some(r => !r.success)) {
    console.log('\nâŒ Some test suites failed. Check the logs above for details.');
    process.exit(1);
  } else {
    console.log('\nâœ… All test suites passed successfully!');
    process.exit(0);
  }
}

// Handle process termination
process.on('SIGINT', () => {
  console.log('\n\nâš ï¸  Test execution interrupted by user');
  process.exit(1);
});

process.on('uncaughtException', (error) => {
  console.log('\n\nðŸ’¥ Unexpected error:', error.message);
  process.exit(1);
});

// Run the main function
main().catch(error => {
  console.log('\n\nðŸ’¥ Fatal error:', error.message);
  process.exit(1);
});
